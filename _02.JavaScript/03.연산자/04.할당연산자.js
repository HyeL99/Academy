/*
    할당(대입)연산자 Assignment operator
*/

let a = 1;
a = a + 2;
console.log(a);

a = 1;
a += 2; // a = a + 2; 와 같은 의미
console.log(a);

a -= 1; // a = a - 1; 과 같은 의미
console.log(a);

a *= 3; // a = a * 3; 과 같은 의미
console.log(a);

a /= 2; // a = a / 2; 과 같은 의미
console.log(a);

a %= 2; // a = a % 2; 과 같은 의미
console.log(a);

// 문자열 추가
let b = '오늘부터 ';
b += '우리는'
console.log(b)